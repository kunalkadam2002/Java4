class p198{
   public static void main(String args[]){
    int a,b,c,d;
    for(a=1;a<=5;a++){
       for(b=5;b>=a;b--){
	System.out.print(" ");
       }
       for(c=a;c>=1;c--){
	System.out.print(c);
       }
       for(d=2;d<=a;d++){
	System.out.print(d);
       }
      
      System.out.print("\n");

    }
 
   }
}