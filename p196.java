class p196{
   public static void main(String args[]){
       int a,b,c,d;
       for(a=5;a>=1;a--){
           for(b=a;b<=5;b++){
	System.out.print(" ");
           }
           for(c=a;c>=1;c--){
	System.out.print(c);
           }
           for(d=2;d<=a;d++){
	System.out.print(d);
           }
           System.out.print("\n");
       }
   }
}